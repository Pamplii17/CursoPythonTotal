#En este archivo se estudiaran los strings

print("Javier")

#El simbolo "+" concatena cadenas de caracteres
print("Hola " + "Javier")
print("Hola" + " " + "Javier")

#Con "\"" se pueden poner las comillas sin que salga error
print("Me llamo \"Javier\"")

#Con "\n" se puede imprimir un salto de linea
print("Esta es una linea\nY esta es otra linea")

#Con "\t" se puede tabular
print("\tEsta linea esta tabulada")

#Con "\\" puedo imprimir una barra invertida
print("Este signo \\ es una barra invertida")